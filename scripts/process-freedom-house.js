// Process data and create JSON mapping
const freedomHouseData = {};

// The 2024 status is in the last column (index should be the last Status column)
// Skip first 3 rows (headers)
for (let i = 3; i < data.length; i++) {
  const row = data[i];
  if (!row || row.length === 0) continue;

  const countryName = row[0];
  // The last column should be the 2024 status
  const status = row[row.length - 1];

  if (countryName && status && countryToISO3[countryName]) {
    const iso3 = countryToISO3[countryName];

    // Map status values
    if (status === 'F' || status === 'Free' || status === 'FREE') {
      freedomHouseData[iso3] = 'Free';
    } else if (status === 'PF' || status === 'Partly Free' || status === 'PARTLY FREE') {
      freedomHouseData[iso3] = 'Partly Free';
    } else if (status === 'NF' || status === 'Not Free' || status === 'NOT FREE') {
      freedomHouseData[iso3] = 'Not Free';
    }
  }
}

// Write to JSON file
fs.writeFileSync('public/freedom-house-data.json', JSON.stringify(freedomHouseData, null, 2));

